#include	<std_decs.h>
/*=============================================================
| Clear screen where box to be drawn + if _box then draw box. |
=============================================================*/
void
cl_box(int x, int y, int h, int v)
{
	int	i,
		j;

	if ( h > 1 )
	{
		j = v ;		
		i = y + 1;
	}
	else
	{
		j = v + 2;
		i = y;
	}
		
	while ( j-- )
	{
		if ( h > 1)
			print_at( i, x + 1 , "%*.*s", h - 2, h - 2, " ");
		i++;
	}
	box( x,y,h,v );
}
